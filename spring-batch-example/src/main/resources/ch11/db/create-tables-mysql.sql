DROP TABLE IF EXISTS skipbills;
DROP TABLE IF EXISTS t_credit;
DROP TABLE IF EXISTS t_destcredit;

create table skipbills
(
  line character(9) not null,
  content character varying(512),
  constraint skipbills_pkey primary key (line)
);

CREATE TABLE t_credit
    (ID VARCHAR(64),
        ACCOUNTID VARCHAR(20),
        NAME VARCHAR(10),
        AMOUNT NUMERIC(10,2),
        DATE VARCHAR(20),
        ADDRESS VARCHAR(128),
        FLAG VARCHAR(10),
        primary key (ID)
    )
    ENGINE=InnoDB DEFAULT CHARSET=utf8;
    
CREATE TABLE t_destcredit
    (ID VARCHAR(64),
        ACCOUNTID VARCHAR(20),
        NAME VARCHAR(10),
        AMOUNT NUMERIC(10,2),
        DATE VARCHAR(20),
        ADDRESS VARCHAR(128),
        primary key (ID)
    )
    ENGINE=InnoDB DEFAULT CHARSET=utf8;
    
    
